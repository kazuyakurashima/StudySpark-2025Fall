-- ヘルパー関数: study_content_type_id を取得（見つからない場合はエラー）
CREATE OR REPLACE FUNCTION pg_temp.ct_id(
  p_grade INTEGER, p_subject_id BIGINT, p_course course_level, p_name TEXT
) RETURNS BIGINT AS $fn$
DECLARE
  v_id BIGINT;
BEGIN
  SELECT id INTO v_id FROM public.study_content_types
  WHERE grade = p_grade AND subject_id = p_subject_id
    AND course = p_course AND content_name = p_name;
  IF v_id IS NULL THEN
    RAISE EXCEPTION 'study_content_type が見つかりません: grade=%, subject_id=%, course=%, name=%',
      p_grade, p_subject_id, p_course, p_name;
  END IF;
  RETURN v_id;
END;
$fn$ LANGUAGE plpgsql STABLE;

